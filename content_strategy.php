          <p>
            Youth from diverse communities lack exposure to each other and to
            new ideas, leading to closed, unreflective minds and prejudices
            towards different people and views. This restricts them from taking
            initiatives and exploring creative options in career and social
            action.<br/> 
            Addressing this learning gap, SAHER creates a space where young people discover
            themselves, identify their orientation towards conflict handling and develop
            their perspectives around respect for diversity, social action and career
            choices.<br/> 
            We work with youth from colleges and communities, adolescents from schools in
            the community and with people at large. <br/><br/>
            <b>Working Principles</b><br/>
            Learning to Unlearn<br/>
            Learning collectively amidst diversity<br/>
            Developing Critical thinking through self reflection and analysing connection with the world around<br/>
            Building peace through citizenship action<br/>
            <br/> 
            SAHER's mission is to enable young people to think critically and reflect on
            their actions to foster respect for diversity and impact themselves and the
            society positively<br/>
            ...Creating a World where Differences are
            Recognised, Accepted and Appreciated (Our Vision) 
            <br/><br/>
            <b>We believe</b><br/>
            An openness to learn from multiple realities broadens our perspective<br/>
            Acceptance and appreciation of differences makes the world a beautiful and multilayered mosaic<br/>
            Freedom of choice leads to a greater sense of ownership towards shared spaces<br/> 
            <br/>
            <b>We assume</b><br/>
            Young people have immense potential to think constructively to bring
            about positive change<br/>
            Every individual has an active citizen in him/her waiting to be
            revealed through the appropriate sparks <br/>
            The ability to question absolute power and the zeal to initiate
            positive action creates a society that is more just and equitable
            <br/><br/>
            <b>Core Strategies</b><br/>
            Working with adolescents and youth: To provide spaces to young
            people to interact and explore their abilities, such that they begin
            their journeys as agents of social change. The attempt here is to
            ensure that the youth recognize their abilities and identify their
            rights to be in a position to realize their mutual relationship with
            each other and the surrounding environment <br/>
            Building awareness: To address the issue of social peace through
            civic awareness and facilitation of collective action that caters to
            the common needs across communities, irrespective of socially and
            politically constructed differences and identities <br/>
            Bridging the gap between communities: To develop an understanding of
            a heterogeneous social context by breaking stereotypes and
            undertaking activities that involve people from various
            backgrounds<br/>
          </p>
