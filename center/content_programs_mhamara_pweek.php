          <h1>Annual Peace Week</h1>    
          <p>
            We are a country where more than 100 languages, a little more than twenty
            religious beliefs and a multitude of cuisines come together each day to stir up a
            pretty mosaic. And yet, these are the same wedges that are driven into the
            mosaic to polarize people and propagate a vision of citizenship that traps the
            'self' in borders drawn around identities that exclude the 'other'<br />
            <br />
            
            One such effort towards fostering interaction across communities and building peace is the Annual
            Peace Week with the following objectives:
            <ul>
              <li>
                Creating a platform for children from different backgrounds (English, Urdu, Hindi,
                Marathi and Gujarati media schools)to play, interact and know each other
              </li>
              <li>
              Interweaving a spirit of coming together and breaking barriers of identities such as
              religion, class and gender and making it a vital part of children's education as they
              grow up
              </li>
              <li>
              Encouraging volunteerism among youth to organise such events that create positive
              spaces for expression and social action
              </li>
              <li>
              Creating spaces for the community to interact and exchange to foster mutual respect
              and understanding through sports
              </li>
            </ul>
