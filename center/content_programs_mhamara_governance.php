            The Right to Information Act came into force on 13 October 2005. The Right to
            Information Act 2005 is a law enacted by the Parliament of India giving citizens
            of India access to records of the Central Government and State Governments. The
            Act applies to all States and Union Territories of India, except the State of
            Jammu and Kashmir - which is covered under a State-level law. Under the
            provisions of the Act, any citizen (including the citizens within J&K) may
            request information from a "public authority" (a body of Government or
            "instrumentality of State") which is required to reply expeditiously or within
            thirty days. The Act also requires every public authority to computerise their
            records for wide dissemination and to proactively publish certain categories of
            information so that the citizens need minimum recourse to request for
            information formally. 
